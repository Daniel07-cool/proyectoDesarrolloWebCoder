class Producto {
    ///generar atributos/caracteristicas/propiedades del objeto
    nombre;
    tipo;
    precio;
    cantidad;
    urlImagen;


    constructor(nombre, tipo, precio, cantidad, urlImagen) {
        this.nombre   = nombre;
        this.precio   = precio;
        this.cantidad = cantidad;
        this.tipo     = tipo;
        this.urlImagen = urlImagen;
    }
}




